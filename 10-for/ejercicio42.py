# Imprimir todos los números impares que hay entre 1 y 100.

for x in range(1,100,2):
    print(x)

'''range puede tener también tres parámetros, el primero indica el 
   valor inicial que tomará la variable x, el segundo parámetro el 
   valor final (que no se incluye) y el tercer parámetro indica 
   cuanto se incrementa cada vuelta x.'''